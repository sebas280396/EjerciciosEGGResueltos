package Entidad;

import java.util.Scanner;

/**
 *
 * @author sebas
 */
public abstract class Edificio {
       protected int ancho;
    protected int alto;
    protected int largo;

    public Edificio() {
    }
Scanner leer = new Scanner(System.in);
    public Edificio(int ancho, int alto, int largo) {
        this.ancho = ancho;
        this.alto = alto;
        this.largo = largo;
    }

    public int getAncho() {
        return ancho;
    }

    public void setAncho(int ancho) {
        this.ancho = ancho;
    }

    public int getAlto() {
        return alto;
    }

    public void setAlto(int alto) {
        this.alto = alto;
    }

    public int getLargo() {
        return largo;
    }

    public void setLargo(int largo) {
        this.largo = largo;
    }
    public abstract void calcularSuperficie (int ancho, int largo);
    public abstract void calcularVolumen (int ancho, int largo, int alto);
}

